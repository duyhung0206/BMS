     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        if($request->has('period_default')){
            Setting::where('path', 'period_default')->update([
                'value' => $request->input('period_default')
            ]);
        }

        if($request->has('theme_default')){
            Setting::where('path', 'theme_default')->update([
                'value' => $request->input('theme_default')
            ]);
        }
    }

